#===============================================================================
# **
#-------------------------------------------------------------------------------
#
#===============================================================================
class Playtime
  attr_reader :hour # ore di gioco
  attr_reader :min  # minuti di gioco
  attr_reader :sec  # secondi di gioco
  #--------------------------------------------------------------------------
  # *
  #--------------------------------------------------------------------------
  def initialize(seconds)
    @hour = seconds / 60 / 60
    @min = seconds / 60 % 60
    @sec = seconds % 60
  end
  #--------------------------------------------------------------------------
  # *
  #--------------------------------------------------------------------------
  # @return [String]
  def to_s; sprintf('%02d:%02d:%02d', hour, min, sec); end
end